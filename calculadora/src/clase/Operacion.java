package clase;

public class Operacion {

public double sumar(double n1, double n2){
	    
        double suma = n1 + n2;
        return suma;
    }

public double restar(double n1, double n2){
    
    double resta = n1 - n2;
    return resta;
}    

public double multiplicar (double n1, double n2){
        double producto = n1 * n2;
        return producto;
    }
    
}

